echo "Количество аргументов: $#"
#!/bin/bash

# Скрипт для подсчета количества и среднего арифметического аргументов

if [ $# -eq 0 ]; then
    echo "Ошибка: нет аргументов"
    exit 1
fi

count=$#
sum=0

# Суммируем все аргументы
for num in "$@"; do
    # Проверяем, что аргумент - число
    if ! [[ "$num" =~ ^-?[0-9]+$ ]]; then
        echo "Ошибка: '$num' не является числом"
        exit 1
    fi
    sum=$((sum + num))
done

# Вычисляем среднее арифметическое
average=$(echo "scale=2; $sum / $count" | bc)

echo "Количество чисел: $count"
echo "Сумма чисел: $sum"
echo "Среднее арифметическое: $average"
